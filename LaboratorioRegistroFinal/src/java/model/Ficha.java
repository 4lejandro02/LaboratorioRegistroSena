public class Ficha {
    int codFicha;
    int cantiaprendices;
    int nombrepro;
    int nombreapre;

    public Ficha() {
        
    }

    public int getCodFicha() {
        return codFicha;
    }

    public void setCodFicha(int codFicha) {
        this.codFicha = codFicha;
    }

    public int getCantiaprendices() {
        return cantiaprendices;
    }

    public void setCantiaprendices(int cantiaprendices) {
        this.cantiaprendices = cantiaprendices;
    }

    public int getNombrepro() {
        return nombrepro;
    }

    public void setNombrepro(int nombrepro) {
        this.nombrepro = nombrepro;
    }

    public int getNombreapre() {
        return nombreapre;
    }

    public void setNombreapre(int nombreapre) {
        this.nombreapre = nombreapre;
    }

}