package model;

public class Aprendiz
{
    int id,documento,telea;
    String nombrea,apellidoa,correoa;

    public Aprendiz() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDocumento() {
        return documento;
    }

    public void setDocumento(int documento) {
        this.documento = documento;
    }

    public int getTelea() {
        return telea;
    }

    public void setTelea(int telea) {
        this.telea = telea;
    }

    public String getNombrea() {
        return nombrea;
    }

    public void setNombrea(String nombrea) {
        this.nombrea = nombrea;
    }

    public String getApellidoa() {
        return apellidoa;
    }

    public void setApellidoa(String apellidoa) {
        this.apellidoa = apellidoa;
    }

    public String getCorreoa() {
        return correoa;
    }

    public void setCorreoa(String correoa) {
        this.correoa = correoa;
    }
    
          
    
}
