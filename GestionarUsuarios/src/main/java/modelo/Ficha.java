package modelo;

public class Ficha {
    int codigopro;
    int codigoapre;
    int cantidadpro;
    String nombrepro;

    public Ficha() {
    }

    public int getCodigopro() {
        return codigopro;
    }

    public void setCodigopro(int codigopro) {
        this.codigopro = codigopro;
    }

    public int getCodigoapre() {
        return codigoapre;
    }

    public void setCodigoapre(int codigoapre) {
        this.codigoapre = codigoapre;
    }

    public int getCantidadpro() {
        return cantidadpro;
    }

    public void setCantidadpro(int cantidadpro) {
        this.cantidadpro = cantidadpro;
    }

    public String getNombrepro() {
        return nombrepro;
    }

    public void setNombrepro(String nombrepro) {
        this.nombrepro = nombrepro;
    }

}